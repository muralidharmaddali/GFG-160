class Solution {
  public:
    vector<int> productExceptSelf(vector<int>& arr) {
        // code here
        int n=arr.size(),prod=1,cnt=count(arr.begin(),arr.end(),0);
        if(cnt>1) return vector<int>(n,0);
        for(int j:arr) if(j) prod*=j;
        for(int i=0;i<n;i++)
        {
            arr[i]=cnt? (arr[i]?0:prod):prod/arr[i];
        }
        return arr;
    }
};


